<center><h2 style="color:#933;">The Graduate Record Examinations(GRE)</h2></center><br />
<p><b><font size="+1">The Graduate Record Examinations (GRE) General Test is a standardized test that graduate schools use to evaluate candidates. Over one thousand graduate programs around the world require applicants to submit GRE scores in order to be considered for admission. In addition, the GRE is often used to help determine whether a student is awarded graduate fellowships or graduate research or teaching positions. Statistics show that a combination of GRE score and undergraduate grade point average is generally a better predictor of first-year graduate school grades than is undergraduate grade point average alone.</font></b></p>
 </div><br />
 <div class="row">
 <p ><b><font size="+1">Test Format
The GRE consists of six sections:</p>
<ol>
<li>One Analytical Writing Section – 2 writing tasks; 30 minutes per task, 60 minutes total.</li>
<li>	Two Quantitative Multiple-Choice Sections – 35 minutes per section; approximately 20 questions per section.</li>
<li>	Two Verbal Multiple-Choice Sections – 30 minutes per section; approximately 20 questions per section.</li>
</ol>
</font></b>
