<h3 style="color:#933;"><b> The Verbal section:</b></h3>
<p><font size="+1">Verbal Reasoning questions appear in several formats, each of which is discussed in detail in the corresponding sections linked to below. About half of the measure requires you to read passages and answer questions on those passages. The other half requires you to read, interpret and complete existing sentences, groups of sentences or paragraphs.</p></font><br />
 <h4 style="color:#933;"><b>Reading Comprehension</b></h4>
<p><font size="+1">Reading comprehension on the GRE consists of a passage of up to 350 words, followed by a cluster of at least two questions that pertain to that passage. These questions frequently require students to summarize the author’s main point, strengthen or weaken an argument made in the passage, or identify what must be true based on the facts presented.</font></p><br />
 <h4 style="color:#933;"><b>Text Completion</b></h4>
<p><font size="+1">These questions consist of a short passage that contains one, two, or three blanks; each blank represents a word or short phrase that is missing from the passage. Students must select exactly one correct answer choice from among the three to five provided for each blank. A student does not receive credit for a Text Completion question unless he or she selects the correct answer choice for every blank in the passage.</font></p><br />
<h4 style="color:#933;"><b>Sentence Equivalence</b></h4>
<p><font size="+1">These questions consist of a single sentence that contains a single blank. Students must select the two answer choices from among the six provided that both (1) fit coherently into the sentence, and (2) produce sentences that are equivalent in meaning. Like Text Completion questions, these questions measure the ability to determine overall meaning from contextual clues. As with all multiple-selection questions on the GRE, students receive no credit unless they select all and only those answer choices that are correct.</font></p>
