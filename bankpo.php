 <p ><b><font size="+1" style="color:#933;">Common Written Examination (CWE) </font></b></p>
 <p><font size="+1">IBPS CWE is one among the most competitive examinations in India with a selection ratio of 1 out of 1000. Candidates receive the date and venue of the exam via a call letter, which will be available for download a few days after they register at the IBPS website. The exam uses negative marking for false answers.
As of 2015, the process has changed to a two-tier system. Tier I is an easier exam where a larger number of candidates are expected to pass, and tier II has a higher level of difficulty. Candidates who pass both stages of CWE will be called for the interview.
The exam syllabus covers topics such as reasoning, general awareness, English language, quantitative aptitude, computer knowledge and professional knowledge.
</font></p><br />

 <p ><b><font size="+1" style="color:#933;">IBPS Specialist Officers</font></b></p>
<p><font size="+1"> There are multiple positions under IBPS Specialist officers, some of them are I. T. Officers (Scale I), Agricultural Field Officers (Scale I), Rajbhashas Adhikari (Scale I), Law Officers (Scale I), HR / Personnel Officers (Scale I), and Marketing Officers (Scale I). Registration for the posts starts in November, while the exam is conducted in subsequent January and final result is declared in April. The exam is of 200 marks consisting of 200 questions and it has 75 questions of professional knowledge.</font></p>
<center><img src="new/Bank-Banner-2.jpg" /></center><br />

 <p ><b><font size="+1" style="color:#933;">Final Selection</font></b></p>
 <p><font size="+1">IBPS forms a rank list once the exam and interview phases end. The same will be used to fill existing and upcoming vacancies in the participating banks for a period of one year from the date of announcement of the final results.
IBPS does not announce the final cut-off marks until the entire process of CWE and the interview is over. This often leaves candidates guessing the expected cut-off marks and not being able to know the exact cut-offs. The process of cut-offs is also then divided as per states. Which is based on fact that if a certain state has less number of vacancies, then the cut-off for that state is likely to go up and if a state has more number of vacancies, then the cut-off can be lower.</font></p>

