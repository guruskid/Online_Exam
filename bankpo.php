<center><h2 style="color:#933;">Banking Personnel Selection (IBPS)</h2></center><br />
<p><font size="+1">IBPS is envisioned as self-governed academic and research oriented Institute, with a mission of enhancing human-resource development through personnel assessment.In 2011, IBPS announced a Common Written Examination (CWE) for the recruitment of officers and clerks in Indian banks. IBPS CWE is now mandatory for anyone who seeks an employment in 29 public sector and regional rural banks. IBPS periodically accepts exam applications from candidates at their website, and the exams are organized at various locations in the country in online mode. Aspirants have to go through an interview process after passing the online test. On the basis of the interview, candidates are placed in regional rural banks.</font></p>
